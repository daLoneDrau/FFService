package com.osrapi.models.ff;

import java.util.List;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.JoinTable;
import javax.persistence.OneToMany;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;

import org.hibernate.annotations.Fetch;
import org.hibernate.annotations.FetchMode;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * @author drau
 */
@JsonInclude(JsonInclude.Include.NON_EMPTY)
@Entity
@Table(name = "room", schema = "ff")
public final class FFRoomEntity {
    /** the code. */
    @Column(name = "code")
    @JsonProperty("code")
    @NotNull
    private String code;
    /**
     * the list of {@link FFCommandEntity}s associated with this
     * {@link FFRoomEntity}.
     */
    @OneToMany(targetEntity = FFCommandEntity.class,
            fetch = FetchType.EAGER)
    @Fetch(FetchMode.SELECT)
    @JoinTable(name = "room_commands_lookup", schema = "ff",
            joinColumns = @JoinColumn(name = "room_id",
                    referencedColumnName = "room_id"),
            inverseJoinColumns = @JoinColumn(name = "command_id",
                    referencedColumnName = "command_id"))
    @JsonProperty("commands")
    private List<FFCommandEntity> commands;
    /**
     * the primary key - an autogenerated id (unique for each user in the db).
     */
    @Id
    @Column(name = "room_id")
    @GeneratedValue(strategy = GenerationType.SEQUENCE,
            generator = "room_seq")
    @SequenceGenerator(
            name = "room_seq",
            sequenceName = "ff.room_id_seq",
            allocationSize = 1)
    private Long id;
    /** Creates a new instance of {@link FFRoomEntity}. */
    public FFRoomEntity() {
        super();
    }

    /**
     * Gets the code.
     * @return {@link String}
     */
    public String getCode() {
        return code;
    }
    /**
     * Gets the list of commandss.
     * @return {@link List}<{@link FFCommandEntity}>
     */
    public List<FFCommandEntity> getCommands() {
        return commands;
    }
    /**
     * Gets the id.
     * @return {@link Long}
     */
    public Long getId() {
        return id;
    }

    /**
     * Sets the code.
     * @param val the new value
     */
    public void setCode(final String val) {
        code = val;
    }
    /**
     * Sets the list of commandss.
     * @param val the new value
     */
    public void setCommands(final List<FFCommandEntity> val) {
        commands = val;
    }
    /**
     * Sets the id.
     * @param val the new value
     */
    public void setId(final Long val) {
        id = val;
    }

}
